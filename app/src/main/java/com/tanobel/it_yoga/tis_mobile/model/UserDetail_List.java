public class UserDetail_List {
    String code, nama, email, genre, imgprofil, usertis;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getImgprofil() {
        return imgprofil;
    }

    public void setImgprofil(String imgprofil) {
        this.imgprofil = imgprofil;
    }

    public String getUsertis() {
        return usertis;
    }

    public void setUsertis(String usertis) {
        this.usertis = usertis;
    }
 }

